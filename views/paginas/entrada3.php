    <div class="resumen-propiedad">
      
    <p>
    Después de 9 años de negociaciones el Servicio de Inspección de Sanidad Animal y Vegetal (del inglés APHIS) del Departamento de Agricultura de los Estados Unidos (del inglés USDA) por fin autorizó la importación de aguacate ecuatoriano.
    </p>

    <p>
    Actualmente en Ecuador los agricultores cultivan 2 variedades de aguacate: Hass y Fuerte, para las cuales se tiene una superficie de 1,200 hectáreas y 5,000 hectáreas, respectivamente, distribuidas por todo el país. El Ministerio de Agricultura espera que esta autorización impulse el incremento de la superficie.
    </p>

    <p>
    El objetivo del gobierno, según se indica, es que se alcancen las 10,000 hectáreas de aguacate en 5 años, esperando un rendimiento promedio de 15 toneladas por hectáreas, por lo que en teoría se estaría hablando de que la meta para el país es de 150,000 toneladas anuales, aunque sin definir cuánto sería exportable.
    </p>

    <p>
    De esta manera, los productores ecuatorianos de aguacate buscan incursionan en el mejor mercado en términos de consumo y precios, por lo que su retorno sobre la inversión podría incrementarse considerablemente; aunque claro, hay que recordar que es un mercado muy competido liderado por México y Perú.
    </p>

