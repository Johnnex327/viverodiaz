    <p>
    En los últimos años la tecnología ha permitido una digitalización en todos los ámbitos laborales. El sector primario no es ajeno a los cambios tecnológicos y también se ha visto inmerso en una digitalización.
    </p>

    <p>
    La digitalización de la agricultura nace con el fin de ahorrar y mejorar el manejo, la gestión, la parametrización y la calidad de vida en general de los agricultores. En este post vamos a ver como la digitalización en la agricultura nos puede ayudar y beneficiar en la gestión agrícola de nuestras explotaciones. 
    </p>

    <p>
    ¿Qué objetivos tiene la digitalización de la agricultura? 
    </p>

    <p>
    La digitalización de la agricultura pretende dejar de lado los papeles y registrar todos los datos informáticamente en el cuaderno de campo. Dentro del sector agropecuario este proceso surge con varios objetivos claros en base a unas necesidades preexistentes.
    </p>


    <p>
    Reducir documentos físicos
    </p>

    <p>
    El sector agrícola destaca por una gran carga de burocracia y documentos a papel que en muchos casos dificulta el orden, y la gestión. A diferencia de un despacho, en el campo los papeles se pueden mojar, ensuciar o perder muy fácilmente, además de almacenar una gran cantidad de carpesanos
    </p>

    <p>

    </p>
    Con la digitalización de la agricultura se quiere evitar todos estos papeles y documentos y trabajar de forma digital con el mismo teléfono móvil. A día de hoy, la gran mayoría de personas del sector agrícola lleva en su bolsillo un teléfono móvil de última generación con conexión a internet. Esto permite que la digitalización de la agricultura pueda ser muy fácil y atractiva para todos los agricultores. 
